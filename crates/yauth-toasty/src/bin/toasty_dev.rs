//! Dev-only CLI for generating toasty migrations from yauth-toasty models.
//!
//! This binary is NOT shipped to consumers (not included in published crate).
//! It exists so developers can generate migration diffs after model changes.
//!
//! # Usage
//!
//! ## Generate initial migration (from empty schema):
//! ```bash
//! cargo run -p yauth-toasty --bin toasty-dev --features sqlite,dev-cli -- generate --name initial
//! ```
//!
//! ## Generate subsequent migration (diff from previous snapshot):
//! ```bash
//! cargo run -p yauth-toasty --bin toasty-dev --features sqlite,dev-cli -- generate --name add_passkey_fields
//! ```
//!
//! ## Check migration status:
//! ```bash
//! cargo run -p yauth-toasty --bin toasty-dev --features sqlite,dev-cli -- status
//! ```

use anyhow::{Context, Result};
use sha2::{Digest, Sha256};
use std::fs;
use std::path::PathBuf;

/// Migration history entry stored in history.toml
#[derive(serde::Serialize, serde::Deserialize, Debug)]
struct History {
    migrations: Vec<MigrationEntry>,
}

#[derive(serde::Serialize, serde::Deserialize, Debug)]
struct MigrationEntry {
    name: String,
    checksum: String,
    created_at: String,
}

fn crate_root() -> PathBuf {
    // The binary runs from the crate root via `cargo run -p yauth-toasty`
    PathBuf::from(env!("CARGO_MANIFEST_DIR"))
}

fn toasty_dir() -> PathBuf {
    crate_root().join("toasty")
}

fn history_path() -> PathBuf {
    toasty_dir().join("history.toml")
}

fn migrations_dir() -> PathBuf {
    toasty_dir().join("migrations")
}

fn snapshots_dir() -> PathBuf {
    toasty_dir().join("snapshots")
}

/// Build a Db handle with all yauth models registered.
///
/// Uses an in-memory SQLite database — no actual database connection needed
/// for schema inspection and migration generation.
async fn build_db() -> Result<toasty::Db> {
    let db = toasty::Db::builder()
        .table_name_prefix("yauth_")
        .models(toasty::models!(yauth_toasty::*))
        .connect("sqlite::memory:")
        .await
        .context("failed to build Db with yauth models")?;
    Ok(db)
}

/// Load previous history, or return an empty one.
fn load_history() -> History {
    fs::read_to_string(history_path())
        .ok()
        .and_then(|content| toml::from_str(&content).ok())
        .unwrap_or(History {
            migrations: Vec::new(),
        })
}

/// Compute SHA-256 checksum of file contents.
fn compute_checksum(content: &str) -> String {
    let mut hasher = Sha256::new();
    hasher.update(content.as_bytes());
    format!("sha256:{:x}", hasher.finalize())
}

/// Serialize the db-level schema to a TOML snapshot.
fn schema_to_snapshot(schema: &toasty::schema::db::Schema) -> String {
    let mut out = String::new();
    out.push_str("# Schema snapshot generated by toasty-dev\n");
    out.push_str("# Used to compute diffs for subsequent migrations\n\n");

    for table in &schema.tables {
        out.push_str(&format!("[[tables]]\nname = {:?}\n", table.name));

        // Columns
        out.push_str("columns = [\n");
        for col in &table.columns {
            let mut parts = vec![format!("name = {:?}", col.name)];
            parts.push(format!("ty = {:?}", format!("{:?}", col.ty)));
            if col.nullable {
                parts.push("nullable = true".to_string());
            }
            out.push_str(&format!("    {{ {} }},\n", parts.join(", ")));
        }
        out.push_str("]\n");

        // Primary key
        out.push_str(&format!(
            "primary_key = {:?}\n",
            table
                .primary_key
                .columns
                .iter()
                .map(|c| c.index)
                .collect::<Vec<_>>()
        ));

        // Indexes
        if !table.indices.is_empty() {
            out.push_str("indices = [\n");
            for idx in &table.indices {
                let cols: Vec<_> = idx.columns.iter().map(|c| c.column.index).collect();
                let mut parts = vec![format!("name = {:?}", idx.name)];
                parts.push(format!("columns = {:?}", cols));
                if idx.unique {
                    parts.push("unique = true".to_string());
                }
                out.push_str(&format!("    {{ {} }},\n", parts.join(", ")));
            }
            out.push_str("]\n");
        }

        out.push('\n');
    }

    out
}

/// Generate a new migration by diffing the previous snapshot (or empty schema)
/// against the current model definitions.
async fn cmd_generate(name: &str) -> Result<()> {
    let db = build_db().await?;
    let full_schema = db.schema();
    let target = &full_schema.db;

    // Load previous schema snapshot, or use empty.
    // TODO: For subsequent migrations, load the previous snapshot from
    // snapshots/NNNN_snapshot.toml instead of always diffing from empty.
    let mut history = load_history();
    let previous = toasty::schema::db::Schema::default();

    let hints = toasty::schema::db::RenameHints::new();
    let diff = toasty::schema::db::SchemaDiff::from(&previous, target, &hints);

    if diff.is_empty() {
        println!("No schema changes detected. Nothing to generate.");
        return Ok(());
    }

    // Generate SQL via the driver (Migration is a single-variant enum)
    let toasty::schema::db::Migration::Sql(sql) = db.driver().generate_migration(&diff);

    // Determine sequence number
    let seq = history.migrations.len();
    let migration_name = format!("{seq:04}_{name}");

    // Ensure directories exist
    fs::create_dir_all(migrations_dir())?;
    fs::create_dir_all(snapshots_dir())?;

    // Write migration SQL
    let sql_path = migrations_dir().join(format!("{migration_name}.sql"));
    let sql_header = "-- Generated by toasty-dev from yauth-toasty models\n\
         -- DO NOT EDIT — regenerate with: cargo run --bin toasty-dev --features sqlite,dev-cli -- generate\n\n";
    let full_sql = format!("{sql_header}{sql}");
    fs::write(&sql_path, &full_sql)?;
    println!("  Created {}", sql_path.display());

    // Write snapshot
    let snapshot = schema_to_snapshot(target);
    let snapshot_path = snapshots_dir().join(format!("{migration_name}_snapshot.toml"));
    fs::write(&snapshot_path, &snapshot)?;
    println!("  Created {}", snapshot_path.display());

    // Update history
    let checksum = compute_checksum(&full_sql);
    let now = jiff::Timestamp::now();
    history.migrations.push(MigrationEntry {
        name: migration_name.clone(),
        checksum,
        created_at: now.to_string(),
    });
    let history_content = toml::to_string_pretty(&history)?;
    fs::write(history_path(), &history_content)?;
    println!("  Updated {}", history_path().display());

    println!("\nMigration '{migration_name}' generated successfully.");
    println!("Review the files and commit them alongside your model changes.");

    Ok(())
}

/// Print migration status.
async fn cmd_status() -> Result<()> {
    let history = load_history();

    if history.migrations.is_empty() {
        println!(
            "No migrations found. Run `generate --name initial` to create the initial migration."
        );
        return Ok(());
    }

    println!("Migrations ({}):", history.migrations.len());
    for entry in &history.migrations {
        let sql_path = migrations_dir().join(format!("{}.sql", entry.name));
        let exists = sql_path.exists();
        let status = if exists { "✓" } else { "✗ MISSING" };
        println!("  {status} {} ({})", entry.name, entry.checksum);
    }

    // Validate checksums
    let mut issues = Vec::new();
    for entry in &history.migrations {
        let sql_path = migrations_dir().join(format!("{}.sql", entry.name));
        if let Ok(content) = fs::read_to_string(&sql_path) {
            let actual = compute_checksum(&content);
            if actual != entry.checksum {
                issues.push(format!(
                    "  ✗ {} checksum mismatch: expected {}, got {}",
                    entry.name, entry.checksum, actual
                ));
            }
        }
    }

    if issues.is_empty() {
        println!("\nAll checksums valid.");
    } else {
        println!("\nChecksum issues:");
        for issue in &issues {
            println!("{issue}");
        }
    }

    Ok(())
}

#[tokio::main]
async fn main() -> Result<()> {
    let args: Vec<String> = std::env::args().collect();

    match args.get(1).map(|s| s.as_str()) {
        Some("generate") => {
            let name = args
                .iter()
                .position(|a| a == "--name")
                .and_then(|i| args.get(i + 1))
                .context("usage: toasty-dev generate --name <migration_name>")?;
            cmd_generate(name).await
        }
        Some("status") => cmd_status().await,
        _ => {
            println!("yauth-toasty migration developer tool\n");
            println!("Usage:");
            println!("  toasty-dev generate --name <name>  Generate a new migration");
            println!("  toasty-dev status                  Show migration status");
            Ok(())
        }
    }
}
